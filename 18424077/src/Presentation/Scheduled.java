    public void LoadScheduled(String malop) {
        List<ScheduledObjects> lst = new ScheduledBLL().getElementByMaLop(malop);
        if (!lst.isEmpty()) {
            DefaultTableModel model = (DefaultTableModel) tblThoiKhoaBieu.getModel();
            model.setRowCount(0);
            String[] columnsName = {"STT", "Mã Môn", "Tên Môn", "Phòng Học"};
            model.setColumnIdentifiers(columnsName);
            int i = 1;
            for (ScheduledObjects sc : lst) {
                Vector row = new Vector();
                String number = Integer.toString(i);
                row.add(number);
                row.add(sc.getMaMon());
                SubjectsObjects su = new SubjectsObjectsBLL().GetElementByID(sc.getMaMon());
                row.add(su.getTenMon());
                row.add(sc.getPhongHoc());
                model.addRow(row);
                i++;
            }
        }
    }

