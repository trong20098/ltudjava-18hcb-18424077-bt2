    public static String filename(String str, char sep, char ext) {
        String fullpath = str;
        int dot1 = fullpath.lastIndexOf(ext);
        int sep1 = fullpath.lastIndexOf(sep);
        return fullpath.substring(sep1 + 1, dot1);
    }

    private void btnThoiKhoaBieuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThoiKhoaBieuActionPerformed
        // TODO add your handling code here:
        int returnvalue = fileScheduled.showOpenDialog(this);
        if (returnvalue == JFileChooser.APPROVE_OPTION) {
            File[] files = fileScheduled.getSelectedFiles();
            for (File file : files) {
                try {
                    filename = file.toString();
                    String fi = filename(filename, '\\', ' ');
                    int hocky = Integer.parseInt(filename(filename, 'K', '_'));
                    String namhoc = filename(filename, '_', '.');
                    ClassObjects cl = new ClassObjects();
                    cl.setMaLop(fi);
                    new ClassBLL().Insert(cl);
                    List<ThoiKhoaBieuObjects> lstimportdanhsach = new ThoiKhoaBieuBLL().GetElementImportfileCSV(filename);
                    int size = lstimportdanhsach.size();
                    for(int i = 0; i < size; i++)
                    {
                        ScheduledObjects sc = new ScheduledObjects();
                        sc.setMaLop(fi);
                        sc.setMaMon(lstimportdanhsach.get(i).getMaMon());
                        SubjectsObjects su = new SubjectsObjects();
                        su.setMaMon(lstimportdanhsach.get(i).getMaMon());
                        su.setTenMon(lstimportdanhsach.get(i).getTenMon());
                        sc.setHocky(hocky);
                        sc.setNamHoc(namhoc);
                        sc.setPhongHoc(lstimportdanhsach.get(i).getPhonghoc());
                        new ScheduledBLL().Insert(sc);
                        new SubjectsObjectsBLL().Insert(su);
                    }
                    LoadScheduled(fi);
                    cbbMonHoc.removeAllItems();
                    LoadThoiKhoaBieu();
                    cbbMonHoc.removeItemAt(0);
                    cbbMonHoc.setSelectedItem(fi);
                } catch (Exception ex) {
                    ex.getMessage();
                }
            }
        }
    }//GEN-LAST:event_btnThoiKhoaBieuActionPerformed

